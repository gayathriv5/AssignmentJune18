Console.WriteLine("Multiplication of 3 using for loop");
for (int i = 0; i <= 30; i += 3)
{
    Console.WriteLine(i);
}

Console.WriteLine("Multiplication of 3 using while loop");
int j = 0;
while (j <= 30)
{
    Console.WriteLine(j);
    j = j + 3;
}

